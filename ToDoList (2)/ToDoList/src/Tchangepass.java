    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String username=jLabel5.getText();
          try {
              Class.forName("com.mysql.cj.jdbc.Driver");
              Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/addteacher","root","123456");
              if(newpassword.getText().equals(confirmpassword.getText())){  
              String oldpass="SELECT * FROM teacherlist WHERE Username='"+jLabel5.getText()+"' AND Password='"+oldpassword.getText()+"'";
              PreparedStatement ptst=conn.prepareStatement(oldpass);          
              ResultSet rs=ptst.executeQuery(oldpass);
              if(rs.next()){
                  String sql="UPDATE teacherlist SET Password='"+newpassword.getText()+"' WHERE Username='"+jLabel5.getText()+"'";
                  int k=ptst.executeUpdate(sql);
                  if(k>0){
                      JOptionPane.showMessageDialog(this, "Password Changed Successfully");
                  }else{
                      JOptionPane.showMessageDialog(this, "Password not Changed!");
                  }
              }
              else{
                  JOptionPane.showMessageDialog(this, "Password is not Correct!");
              }
              }else{
                  JOptionPane.showMessageDialog(this, "New Password and Confirm Password dosen't Match!");
              }
              conn.close();
          }  catch (ClassNotFoundException e) {
              JOptionPane.showMessageDialog(this, "Error: MySQL JDBC driver not found.");
          } catch (SQLException e) {
              JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
          }    
    }//GEN-LAST:event_jButton1ActionPerformed

