        String enteredusername=username.getText();
        String enteredpassword=new String(password.getPassword());
                try{
                conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/addteacher","root","123456"); 
            {
                String sql="SELECT * FROM teacherlist WHERE username=? AND password=?";
                ptst=conn.prepareStatement(sql);
                ptst.setString(1,enteredusername);
                ptst.setString(2,enteredpassword);
                
                rs=ptst.executeQuery();
                
                if(rs.next()){
                 JOptionPane.showMessageDialog(null, "Login Successful."); 
                 DashboardT Db = new DashboardT();
                 Db.jLabel1.setText(enteredusername);
                 Db.show(); 
                 dispose();
                }else{
                JOptionPane.showMessageDialog(null, "Login Failed! Please check your Username and Password");
                }
                
                rs.close();
                ptst.close();
                conn.close();
            }
                }  
        catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "An error occoured while trying to log in");
        }
        
