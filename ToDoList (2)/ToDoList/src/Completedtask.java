    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         
         completetable.getColumnModel().getColumn(0).setPreferredWidth(350);
         completetable.getColumnModel().getColumn(1).setPreferredWidth(80);
         completetable.getColumnModel().getColumn(2).setPreferredWidth(50);
         
         try {
                String username=jLabel3.getText();
                Class.forName("com.mysql.cj.jdbc.Driver");
                conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/completedtask","root","123456");
                ptst=conn.prepareStatement("select * from completelist WHERE username=?");
                ptst.setString(1, username);
                rs=ptst.executeQuery();
                
                DefaultTableModel dtm=(DefaultTableModel)completetable.getModel();
                dtm.setRowCount(0);
                while(rs.next()){                     
                        String task=rs.getString("Task");
                        String date=rs.getString("Date");
                        String time=rs.getString("Time");
   
                    dtm.addRow(new Object[]{task,date,time});
                }
                
            } catch (ClassNotFoundException e) {
               JOptionPane.showMessageDialog(this, "Error: MySQL JDBC driver not found.");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());          
            }
    }//GEN-LAST:event_jButton3ActionPerformed

