    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         tasklist.getColumnModel().getColumn(0).setPreferredWidth(20);
         tasklist.getColumnModel().getColumn(1).setPreferredWidth(350);
         tasklist.getColumnModel().getColumn(2).setPreferredWidth(80);
         tasklist.getColumnModel().getColumn(3).setPreferredWidth(60); 
         tasklist.getColumnModel().getColumn(4).setPreferredWidth(50); 
         
          try {
                String username=jLabel22.getText();
                Class.forName("com.mysql.cj.jdbc.Driver");
                conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/teachertask","root","123456");
                ptst=conn.prepareStatement("select * from ttasklist WHERE username=?");
                ptst.setString(1, username);
                rs=ptst.executeQuery();
               
                DefaultTableModel dtm=(DefaultTableModel)tasklist.getModel();
                dtm.setRowCount(0);
                while(rs.next()){
                    
                       String prior=rs.getString("Priority");
                       String task=rs.getString("Task");
                       String subject=rs.getString("Subject");
                       String date=rs.getString("Date");
                       String time=rs.getString("Time");                        
                    
                    dtm.addRow(new Object[]{prior,task,subject,date,time});
                }
                
            } catch (ClassNotFoundException | SQLException e) {
               e.printStackTrace();
            }
    }//GEN-LAST:event_jButton3ActionPerformed

