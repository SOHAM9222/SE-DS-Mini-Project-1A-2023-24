    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try {
          
             Class.forName("com.mysql.cj.jdbc.Driver");          
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/addstudent","root","123456");
               String sql="INSERT INTO studentlist(Username,Name,Password,ID,Email,Branch)VALUES(?,?,?,?,?,?)";
               PreparedStatement ptst=conn.prepareStatement(sql);
               ptst.setString(1, usernametxt.getText());
               ptst.setString(2, nametxt.getText());
               ptst.setString(3,passwordtxt.getText());
               ptst.setString(4,idtxt.getText());
               ptst.setString(5,mailtxt.getText());
               ptst.setString(6,branchtxt.getText());
               int rowsInserted=ptst.executeUpdate();
               if(rowsInserted>0){
               JOptionPane.showMessageDialog(this, "Student Successfully Added");  
                AdminDash Dbt=new AdminDash();
                Dbt.show();
                dispose();
               }else{
                   JOptionPane.showMessageDialog(this,"Error! Task not added Successfully" );
               } 
                conn.close();
                ptst.close();
               
       }  catch (ClassNotFoundException e) {
              JOptionPane.showMessageDialog(this, "Error: MySQL JDBC driver not found.");
       } catch (SQLException e) {
         JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
       }  
    }//GEN-LAST:event_jButton1ActionPerformed

